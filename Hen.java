public class Hen extends DomesticAnimal{
    public Hen(int x, int y) {
        this.price = 100;
        this.ManufacturedProduct = "egg";
        this.remainingLife = 100;
        this.timeRequiredToProduct = 2;
        this.name = "Hen";
        this.OccupiedSpace = 1;
        this.X = x;
        this.Y = y;
    }
}
