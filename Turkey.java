public class Turkey extends DomesticAnimal{
    public Turkey(int x, int y) {
        this.price = 200;
        this.ManufacturedProduct = "feather";
        this.remainingLife = 100;
        this.timeRequiredToProduct = 3;
        this.name = "Turkey";
        this.OccupiedSpace = 2;
        this.X = x;
        this.Y = y;
    }
}
